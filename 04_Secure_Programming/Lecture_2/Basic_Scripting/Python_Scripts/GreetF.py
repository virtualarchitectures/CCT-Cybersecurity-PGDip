# Function to greet a person.
def greet(name):
    """
    This function takes a name as input and prints a greeting message.

    Args:
        name (str): The name of the person to greet.
    """
    print(f"Hello, {name}!")

# Call the greet function with different names to test it.
greet("Alice")
greet("Bob")
greet("Charlie")
greet("David")
greet("Eve")